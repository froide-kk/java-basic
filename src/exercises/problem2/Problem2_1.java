package exercises.problem2;

public class Problem2_1 {
    public static void main(String[] args) {
        int a = 5;
        int b = 3;
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("a" + " + " + "b" + " = " + (a + b));
        System.out.println("a" + " - " + "b" + " = " + (a - b));
        System.out.println("a" + " * " + "b" + " = " + (a * b));
        System.out.println("a" + " / " + "b" + " = " + (a / b));
        System.out.println("a" + " % " + "b" + " = " + (a % b));

//        実行結果
//        a = 5
//        b = 3
//        a + b = 8
//        a - b = 2
//        a * b = 15
//        a / b = 1
//        a % b = 2

    }
}
